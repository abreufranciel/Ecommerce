       <h3> Política de Conduta </h3>
        <p class="text-justify">A PetPlace permite que diversas pessoas iniciem vendas e compras referentes a serviços do ramo pet.
Dependemos muito dos usuários para identificar conteúdos que talvez violem nossas políticas. Após sermos notificados sobre uma possível violação de política, podemos analisar o conteúdo e decidir o que fazer, como restringir o acesso ao conteúdo, removê-lo, impedir que o usuário o imprima e limitar ou impedir o acesso dele. Podemos abrir exceções a essas políticas com base em considerações documentais, educativas ou artísticas, ou quando evitar a adoção de tais medidas traga benefícios relevantes para o público.
        </p>
        <h4>Atividades ilegais</h4>
        <p class="text-justify">Não use nossa plataforma para participar de atividades ilegais ou promover atividades perigosas e ilegais, como a venda de drogas ilícitas ou o tráfico de pessoas.
Também podemos remover os conteúdos que violam as leis locais aplicáveis. Para denunciar um conteúdo que você acredita que deve ser removido com base nas leis locais, <a href="?p=contato" target="_blank">clique aqui</a>.
        </p>
        <h4>Práticas enganosas e maliciosas</h4>
        <p class="text-justify">Não distribua conteúdos que prejudiquem ou interfiram na operação de redes, servidores ou outras infraestruturas da PetPlace ou de outros. Não use nossos serviços para golpes de phishing.
        </p>

    <h4>Informações pessoais ou confidenciais</h4>
    <p class="text-justify">Não distribua informações pessoais e confidenciais de outras pessoas, como números de cartão de crédito, números de identificação nacional confidenciais ou senhas de conta, sem permissão. Não poste ou distribua imagens ou vídeos de menores sem o consentimento necessário dos representantes legais. <a href="?p=contato" target="_blank">Denunciar uma violação</a>.</p>

    <h4>Falsificação de identidade e falsidade ideológica</h4>
    <p class="text-justify">Não use nossos serviços para enganar ou confundir os usuários falsificando a identidade de qualquer pessoa ou organização que você não represente ou ocultando sua propriedade ou finalidade principal.</p>
    <br/>
    <h3>Termos de Serviços</h3>
    <h4>Taxas</h4>
    <p class="text-justify">Não há taxas para cadastramento de contas e nem divulgações de serviços. Porém, para arcar com os gastos de manutenção da plataforma, há apenas uma arrecadação de 0,09% do valor a cada compra finalizada.</p>
   <h4>Agendamento</h4>
   <p class="text-justify">O agendamento do serviço adquirido deve ser realizado via contato direto com o prestador de serviços ofertante, mediante aprovação do pagamento.
    A PetPlace fica isenta de qualquer responsabilidade de efetuar agendamento de serviços.
    </p>
